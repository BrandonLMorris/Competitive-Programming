import java.util.*;

/*
 * Given two integers, a and b, find the sum a+b.
 */

public class AB {
  public static void main(String[] args) {
    Scanner in = new Scanner(System.in);

    // Read in the input
    int a = in.nextInt();
    int b = in.nextInt();

    // Output the result
    System.out.println((a+b));
  }
}
